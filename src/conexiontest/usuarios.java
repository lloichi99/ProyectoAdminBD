    void Modificar() {
    String idUsuario = txtIdUsuario.getText();
    String correoInstitucional = txtUsuario.getText();
    String contrasena = txtContraseña.getText();

    try {
        if (idUsuario.equals("") || correoInstitucional.equals("") || contrasena.equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan ingresar Datos");
            limpiarTabla();
            consultar();
        } else {
            // Verificar si el ID que se desea modificar existe
            String checkIDQuery = "SELECT COUNT(*) FROM usuario WHERE ID_USUARIO=" + idUsuario;
            conec = con.getConnection();
            
            try (Statement st = conec.createStatement()) {
                ResultSet rs = st.executeQuery(checkIDQuery);
                rs.next();
                int rowCount = rs.getInt(1);

                if (rowCount == 0) {
                    JOptionPane.showMessageDialog(null, "El ID que deseas modificar no existe.");
                    limpiarTabla();
                } else {
                    // Si el ID existe, ahora se pueden realizar actualizaciones
                    String updateQuery = "UPDATE usuario SET CORREO_INSTITUCIONAL = ?, CONTRASENA = ? WHERE ID_USUARIO = ?";
                    
                    try (PreparedStatement pstmt = conec.prepareStatement(updateQuery)) {
                        pstmt.setString(1, correoInstitucional);
                        pstmt.setString(2, contrasena);
                        pstmt.setString(3, idUsuario);

                        pstmt.executeUpdate();
                    }

                    JOptionPane.showMessageDialog(null, "Datos de la tabla usuario modificados");
                    limpiarCampos();
                    consultar();
                    
                }
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    } 
}


    
    
