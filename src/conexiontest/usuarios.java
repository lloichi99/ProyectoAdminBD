    // Nuevo método para consultar y cargar datos por ID
    private void consultarUsuarioPorID(int idUsuario) {
    try {
        String sql = "SELECT * FROM usuario WHERE ID_USUARIO = ?";
        conec = con.getConnection();

        try (PreparedStatement pstmt = conec.prepareStatement(sql)) {
            pstmt.setInt(1, idUsuario);
            ResultSet rs = pstmt.executeQuery();

            if (rs.next()) {
                String correoInstitucional = rs.getString("CORREO_INSTITUCIONAL");
                String contrasena = rs.getString("CONTRASENA");

                txtUsuario.setText(correoInstitucional);
                txtContraseña.setText(contrasena);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no encontrado");
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}




    
